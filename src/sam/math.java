package sam;

public class math {
	public static void main(String[] args) {
		
		System.out.println(Math.sqrt(9));
		System.out.println(Math.max(10.0, 20.3));
		System.out.println(Math.min(10.0, 20.3));
		System.out.println(Math.abs(+10.0));
		System.out.println(Math.pow(2,3));
		System.out.println(Math.ceil(10.8));	//Rounds up
		System.out.println(Math.floor(20.3));	//Roudns down
		System.out.println(Math.round(20.3));	//Round
		System.out.println(Math.random()*20);
		System.out.println(Math.pow(2,6));
	}
}
